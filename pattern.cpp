#include<iostream>
using namespace std; 
//left side * pattern
int main ()
{
    int n=5;
    for(int i=0;i<=n;i++){
        for(int j=0;j<=i;j++){
            cout<<"*"<<" ";
        }
        cout<<endl;
        }   
        return 0;

}




